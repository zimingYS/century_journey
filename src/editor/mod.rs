//! # Editor
//!
//! 编辑器层（Editor Layer）。
//!
//! `editor` 为 CenturyJourney 提供开发、调试和内容创作能力，
//! 用于编辑游戏世界、资源和配置，而不是参与正式游戏运行。
//!
//! Editor 建立在 Engine、Content 和 Game 之上，
//! 通过调用已有系统完成编辑操作，而不是重新实现游戏规则。
//!
//! ## 职责
//!
//! Editor 负责所有开发工具和编辑功能，包括但不限于：
//!
//! - 世界编辑（World Editor）
//! - 地形编辑（Terrain Editor）
//! - Structure 编辑
//! - Block 编辑
//! - Entity 编辑
//! - Biome 编辑
//! - 资源浏览
//! - JSON 编辑
//! - Registry 浏览
//! - 调试工具
//! - Gizmo
//! - 可视化工具
//!
//! Editor 描述的是："开发者如何创建和修改游戏内容（How Developers Create Content）"。
//!
//! ## 设计原则
//!
//! Editor 是开发工具，而不是游戏逻辑。
//!
//! Editor 应尽可能复用已有系统：
//!
//! - 调用 Game 修改世界。
//! - 调用 Content 查询资源。
//! - 调用 Engine 提供的基础能力。
//!
//! Editor 不应维护独立的一套游戏数据。
//!
//! ## 模块组织
//!
//! ```text
//! editor/
//! ├── world/            // 世界编辑
//! ├── terrain/          // 地形编辑
//! ├── structure/        // Structure 编辑
//! ├── block/            // Block 编辑
//! ├── entity/           // Entity 编辑
//! ├── biome/            // Biome 编辑
//! ├── asset/            // Asset 浏览
//! ├── registry/         // Registry 浏览
//! ├── gizmo/            // 编辑器 Gizmo
//! ├── inspector/        // 属性面板
//! ├── ui/               // 编辑器界面
//! ├── command/          // Undo / Redo
//! └── debug/            // 调试工具
//! ```
//!
//! ## 架构位置
//!
//! ```text
//!                  App
//!                    │
//!      ┌─────────────┼─────────────┐
//!      ▼             ▼             ▼
//!   Client        Server        Editor
//!      │             │             │
//!      └─────────────┼─────────────┘
//!                    ▼
//!                 Protocol
//!                    │
//!                  Game
//!                 /    \
//!           Content   Shared
//!                 \    /
//!                 Engine
//! ```
//!
//! Editor 与 Client、Server 同属于应用层，
//! 但它面向开发者，而不是玩家。
//!
//! ## 核心设计理念
//!
//! Editor 提供的是 **Tooling（工具）**，
//! 而不是 **Gameplay（游戏玩法）**。
//!
//! 例如：
//!
//! - Editor 放置方块用于编辑，而不是玩家建造。
//! - Editor 修改地形，而不是世界生成。
//! - Editor 编辑配方，而不是执行合成。
//! - Editor 修改实体属性，而不是运行 AI。
//!
//! ## 数据流
//!
//! ```text
//! Developer
//!      │
//!      ▼
//! Editor UI
//!      │
//!      ▼
//! Game / Content
//!      │
//!      ▼
//! World State / Definition
//!      │
//!      ▼
//! Save
//! ```
//!
//! Editor 不直接修改底层数据文件，
//! 而是通过统一接口修改世界状态或内容定义，
//! 保证与正式游戏行为保持一致。
//!
//! ## 可扩展性
//!
//! Editor 应支持未来扩展，例如：
//!
//! - 地图编辑器
//! - 剧情编辑器
//! - Mod 工具
//! - Block 编辑器
//! - Entity 编辑器
//! - 配方编辑器
//! - 粒子编辑器
//! - 动画编辑器
//! - 多人协同编辑
//!
//! 所有编辑能力均应作为独立模块进行扩展。
//!
//! ## 判断标准
//!
//! 当一个功能回答的是：
//!
//! "开发者如何创建、修改或调试游戏内容？"
//!
//! 那么它属于 Editor。
//!
//! 如果回答的是：
//!
//! - 如何实现基础能力？→ Engine
//! - 世界里有什么？→ Content
//! - 游戏规则是什么？→ Game
//! - 如何展示？→ Client
//! - 如何管理服务器？→ Server
//!
//! 那么它不属于 Editor。
//!
//! ## 核心原则
//!
//! Editor 永远不是游戏规则的来源。
//!
//! 它负责：
//!
//! - 创建内容
//! - 编辑内容
//! - 浏览内容
//! - 调试内容
//!
//! 所有编辑操作最终都应通过已有的 Engine、Content 和 Game 完成，
//! 从而保证编辑器与正式游戏共享同一套数据结构和行为逻辑，避免出现"编辑器能做、游戏不能做"或"游戏能做、编辑器不能做"的情况。