//! # Tests
//!
//! 测试层（Test Layer）。
//!
//! `tests` 用于验证 CenturyJourney 各模块的正确性、稳定性和兼容性，
//! 通过自动化测试保证游戏逻辑、网络协议、数据加载以及世界模拟能够持续可靠运行。
//!
//! Tests 不属于游戏运行时的一部分，
//! 它仅用于开发、调试和持续集成（CI）。
//!
//! ## 职责
//!
//! Tests 负责验证整个项目的行为，包括但不限于：
//!
//! - 单元测试（Unit Test）
//! - 集成测试（Integration Test）
//! - 功能测试（Functional Test）
//! - 回归测试（Regression Test）
//! - 网络协议测试
//! - 世界模拟测试
//! - 存档兼容测试
//! - 数据加载测试
//! - 性能基准测试（Benchmark）
//!
//! Tests 描述的是："系统是否按预期工作（Does It Work Correctly）"。
//!
//! ## 不负责
//!
//! Tests 不应该实现任何业务逻辑。
//!
//! Tests 不应该成为正式游戏流程的一部分。
//!
//! Tests 不应该修改运行时代码以适配测试。
//!
//! 如果为了通过测试而修改游戏逻辑，则说明测试设计存在问题。
//!
//! ## 设计原则
//!
//! Tests 应尽可能独立、可重复、可自动执行。
//!
//! 每个测试应只验证一个明确的行为，
//! 避免多个功能耦合在同一个测试中。
//!
//! 测试应覆盖：
//!
//! - 正常流程
//! - 边界情况
//! - 异常输入
//! - 历史 Bug 回归
//!
//! ## 模块组织
//!
//! ```text
//! tests/
//! ├── unit/             // 通用单元测试
//! ├── integration/      // 跨模块集成测试
//! ├── gameplay/         // 游戏规则测试
//! ├── networking/       // 网络同步测试
//! ├── save/             // 存档测试
//! ├── content/          // 内容加载测试
//! ├── world/            // 世界模拟测试
//! ├── benchmark/        // 性能测试
//! ├── regression/       // Bug 回归测试
//! └── common/           // 测试工具与辅助函数
//! ```
//!
//! ## 架构位置
//!
//! ```text
//!                 Tests
//!                    │
//!      ┌─────────────┼─────────────┐
//!      ▼             ▼             ▼
//!    Engine       Content        Game
//!      │             │             │
//!      └─────────────┼─────────────┘
//!                    ▼
//!          Client / Server / Protocol
//! ```
//!
//! Tests 不属于任何业务层，
//! 它可以验证整个架构中的所有模块。
//!
//! ## 核心设计理念
//!
//! Tests 提供的是 **Verification（验证）**，
//! 而不是 **Implementation（实现）**。
//!
//! 例如：
//!
//! - Tests 验证方块放置，而不是实现放置。
//! - Tests 验证战斗结果，而不是计算伤害。
//! - Tests 验证网络同步，而不是发送 Packet。
//! - Tests 验证存档一致性，而不是实现存档。
//!
//! ## 测试分类
//!
//! 项目采用分层测试策略：
//!
//! - Unit Test：验证单个模块。
//! - Integration Test：验证多个模块协作。
//! - Regression Test：验证历史 Bug 不再出现。
//! - Benchmark：验证性能是否满足要求。
//!
//! 随着项目发展，应逐步提高自动化测试覆盖率，
//! 确保架构重构不会破坏已有功能。
//!
//! ## 判断标准
//!
//! 当一个文件的唯一目的是：
//!
//! "验证系统是否正确工作"
//!
//! 那么它属于 Tests。
//!
//! 如果它属于游戏运行时的一部分，则不应放入 Tests。
//!
//! ## 核心原则
//!
//! Tests 是整个项目质量保障体系的一部分。
//!
//! 每一次功能开发、架构调整或重构，
//! 都应能够通过 Tests 验证其行为保持一致。
//!
//! 自动化测试应成为重构的安全网，而不是开发完成后的补充工作。

